class Mlt_ECA(nn.Module):
    def __init__(self, channel, b=1, gamma=2):
        torch.use_deterministic_algorithms(False)   # 解决warning
        super(Mlt_ECA, self).__init__()
        kernel_size = int(abs((math.log(channel, 2) + b) / gamma))
        kernel_size = kernel_size if kernel_size % 2 else kernel_size + 1
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.max_pool = nn.AdaptiveMaxPool2d(1)
        self.conv = nn.Conv1d(1, 1, kernel_size=kernel_size, padding=(kernel_size - 1) // 2, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_pooled = self.avg_pool(x)
        conv_avg = self.conv(avg_pooled.squeeze(-1).transpose(-1, -2)).transpose(-1, -2).unsqueeze(-1)

        max_pooled = self.max_pool(x)
        conv_max = self.conv(max_pooled.squeeze(-1).transpose(-1, -2)).transpose(-1, -2).unsqueeze(-1)

        sigmoid_out = self.sigmoid(conv_avg + conv_max)

        combined_att = x * sigmoid_out.expand_as(x)
        return combined_att


